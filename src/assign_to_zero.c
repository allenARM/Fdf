#include "fdf.h"

void	assign_to_zero(t_fdf *fdf)
{
	fdf->x1 = 0;
	fdf->x2 = 0;
	fdf->y1 = 0;
	fdf->y2 = 0;
	fdf->anglex = 0.523599;
	fdf->angley = 0.523599;
}
