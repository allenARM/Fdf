#include "fdf.h"

int		find_len(char *s)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i])
	{
		while (s[i] && s[i] == ' ')
			i++;
		j++;
		while (s[i] && s[i] != ' ')
			i++;
	}
	return (j);
}
